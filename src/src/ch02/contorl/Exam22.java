package ch02.contorl;

/**
 * @Date : 2023. 1. 3.
 * @Author :이준성
 * @Description : 반복문 (do ~ while, while 중첩)
 * 
 */
public class Exam22 {

	public static void main(String[] args) {
		
		
		int x = 20;
		
		while (x > 50) {
			System.out.println(x);
		}
		
		do {
			System.out.println(x);
		}while (x > 50);
		
	}
}

